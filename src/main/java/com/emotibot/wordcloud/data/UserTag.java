package com.emotibot.wordcloud.data;

public class UserTag
{
    String name;
    Double value;
    public String getName()
    {
        return name;
    }
    public void setName(String name)
    {
        this.name = name;
    }
    public Double getValue()
    {
        return value;
    }
    public void setValue(Double value)
    {
        this.value = value;
    }
    
}
